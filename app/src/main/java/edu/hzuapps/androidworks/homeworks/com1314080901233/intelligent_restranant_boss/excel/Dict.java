package com.example.intelligent_restranant_boss.excel;

public class Dict {

	private int id;
	private String name;
	private int number;
	private float price;
	private float totle;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getNumber() {
		return number;
	}
	public void setNumber(int number) {
		this.number = number;
	}
	public float getPrice() {
		return price;
	}
	public void setPrice(float price) {
		this.price = price;
	}
	public float getTotle() {
		return totle;
	}
	public void setTotle(float totle) {
		this.totle = totle;
	}
	
	

}
